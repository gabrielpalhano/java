class Treino {
	public static void main(String[] args) {
		int g = 1;
		int p = 1;
			
			do{
				System.out.println(g);
				++g;
			} while (g < 1);
			
			while (p < 1) {
				
				System.out.println(p);
				++p;
			}
	}
}

